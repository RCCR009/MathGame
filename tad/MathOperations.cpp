#include "MathOperations.h"

int MathOperations::add(int num, int num2) {
    return num+num2;
}

int MathOperations::subtract(int num, int num2) {
    return num-num2;
}

int MathOperations::multiplication(int num, int num2) {
    return num*num2;
}

double MathOperations::split(int num, int num2) {
    return num/num2;
}
